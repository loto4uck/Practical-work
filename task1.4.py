# Програма для обробки пробілів на початку та в кінці рядка

# Зберігаємо ім'я з пробілами
name = "   Іван   "
print(f"Original: '{name}'")  # Виводимо ім'я з пробілами

# Використовуємо функції для видалення пробілів
print(f"Left stripped: '{name.lstrip()}'")  # Видаляємо пробіли зліва
print(f"Right stripped: '{name.rstrip()}'")  # Видаляємо пробіли справа
print(f"Fully stripped: '{name.strip()}'")  # Видаляємо пробіли з обох сторін
