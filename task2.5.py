professions = ['Engineer', 'Doctor', 'Teacher', 'Artist', 'Musician']
sports = ['Football', 'Basketball', 'Tennis', 'Swimming', 'Cycling']
family_members = ['Mother', 'Father', 'Brother', 'Sister', 'Grandmother']
oceans = ['Pacific', 'Atlantic', 'Indian', 'Arctic', 'Southern']

# Використання функцій списку
professions.sort()
sports.reverse()
family_members.append('Uncle')
oceans.insert(0, 'Southern Ocean')

print("Sorted professions:", professions)
print("Reversed sports:", sports)
print("Family members:", family_members)
print("Updated oceans:", oceans)
